#include <iostream>
using namespace std;

int main(){
    int a = 12;
    cout<<"Size of int is "<<sizeof(a)<<"\n";

    float b;
    cout<<"Size of float is "<<sizeof(b)<<"\n";

    char c;
    cout<<"Size of char is "<<sizeof(c)<<endl;

    bool d;
    cout<<"Size of bool is "<<sizeof(d)<<endl;

    short int e;
    cout<<"Size of short int is "<<sizeof(e)<<endl;

    long int f;
    cout<<"Size of long int is "<<sizeof(f)<<endl;
}